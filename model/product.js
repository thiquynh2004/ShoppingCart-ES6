function Product(id, name, price, screen, backCamera, frontCamera, image, desc, type, quantity){
    this.id = id;
    this.name = name;
    this.price = price;
    this.screen = screen;
    this.backCamera = backCamera;
    this.frontCamera = frontCamera;
    this.image = image;
    this.desc = desc;
    this.type = type;
    this.quantity = quantity;
}